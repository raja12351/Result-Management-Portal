    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try{
            String rollno = jTextField4.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","1325");
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from result_management.results inner join result_management.students where result_management.students.rollno='"+rollno+"' and result_management.results.rollno='"+rollno+"'");
            if(rs.next()){
                jTextField1.setText(rs.getString(7));
                jTextField2.setText(rs.getString(8));
                jTextField3.setText(rs.getString(9));
                jTextField5.setText(rs.getString(11));
                jTextField6.setText(rs.getString(12));
                
                jTextField7.setText(rs.getString(2));
                jTextField8.setText(rs.getString(3));
                jTextField9.setText(rs.getString(4));
                jTextField10.setText(rs.getString(5));
                jTextField11.setText(rs.getString(6));
                
                int s1 = Integer.parseInt(jTextField7.getText());
                int s2 = Integer.parseInt(jTextField8.getText());
                int s3 = Integer.parseInt(jTextField9.getText());
                int s4 = Integer.parseInt(jTextField10.getText());
                int s5 = Integer.parseInt(jTextField11.getText());
                
                int total = s1+s2+s3+s5+s4;
                jTextField12.setText(String.valueOf(total));
                
                int avg = total / 5;
                if(avg>=33){
                    jTextField13.setText("Pass");
                }
                else{
                    jTextField13.setText("Fail");
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

