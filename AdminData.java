    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String father = jTextField3.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String rollno = jTextField4.getText();
        String branch = (String)jComboBox1.getSelectedItem();
        String course = (String)jComboBox2.getSelectedItem();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","1325");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into result_management.students(name,gender,father,rollno,course,branch) values ('"+name+"','"+gender+"','"+father+"','"+rollno+"','"+course+"','"+branch+"')");
            JOptionPane.showMessageDialog(null,"Data Updated SuccessFully.");
            setVisible(false);
            new AdminData().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

